#Q6. Write a Program to input two angles from user and find third angle of the triangle.

#Take input
Angle1= float (input("Enter first angle of triangle :"))
Angle2= float(input("Enter Second angle of triangle :"))

#Perform Operation
#Third Triangle = 180'-(Angle1 + Angle2)
Angle3= 180-(Angle1 + Angle2)


#display output
print("Third angle of Triangle :",Angle3)