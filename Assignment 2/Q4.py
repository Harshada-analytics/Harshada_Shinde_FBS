#Q2. Write a program to calculate area of triangle and rectangle.


#calculatte Area of Rectangle

length = float(input("Enter length for Rectangle :"))
width = float(input("Enter width for Rectangle :"))

Rectangle = length * width 
print("Area of Rectangle :", Rectangle)


#calculate area of Triangle 

base = float(input("Enter Base for Triangle:"))
height = float(input("Enter height for Triangle: :"))

Triangle = 1/2 * base * height 
print(Triangle)